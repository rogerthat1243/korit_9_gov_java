package com.korit.study.ch02;

public class Operator02 {
    public static void main(String[] args) {
        int i = 0;
        System.out.println(i);
        i = i + 1;
        System.out.println(i);
        i += 1;
        System.out.println(i);
        ++i;
        System.out.println(i);
        i++;
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i++ + 10);
        System.out.println(i);
    }
}
